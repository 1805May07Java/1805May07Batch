package com.rev.fh;

public class FloatHolder {
	
	public float f1 = 7.7f;
	public float f2 = 7.3f;
	
	public FloatHolder(float f1, float f2) {
		super();
		this.f1 = f1;
		this.f2 = f2;
	}
	
	
	public float getF1() {
		return f1;
	}
	public void setF1(float f1) {
		this.f1 = f1;
	}
	public float getF2() {
		return f2;
	}
	public void setF2(float f2) {
		this.f2 = f2;
	}
	
	

}
