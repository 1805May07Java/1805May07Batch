package MultiplePackages;

public class MultiplePackages {
	
	static float f1 = 3.4f;
	static float f2 = 7.7f;
	
	public MultiplePackages(float f1, float f2) {
		super();
		this.f1 = f1;
		this.f2 = f2;
	}

	public float getF1() {
		return f1;
	}

	public void setF1(float f1) {
		this.f1 = f1;
	}

	public float getF2() {
		return f2;
	}

	public void setF2(float f2) {
		this.f2 = f2;
	}
	
	
	

}
